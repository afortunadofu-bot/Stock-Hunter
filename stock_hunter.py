def analyze_us_stocks():
    msg = "🇺🇸 **【美股｜價量潛力雷達】**\n*(美股無每日籌碼，改採價量突破與均線多頭過濾)*\n"
    # 監控重點科技與半導體巨頭 (可自行增刪)
    us_tickers = ["AAPL", "MSFT", "NVDA", "TSLA", "AMD", "META", "GOOGL", "AMZN", "PLTR", "ARM"]
    found = False

    for ticker in us_tickers:
        stock = yf.Ticker(ticker)
        hist = stock.history(period="1mo")
        if len(hist) < 20: continue

        curr_price = hist['Close'].iloc[-1]
        ma_20 = hist['Close'].rolling(window=20).mean().iloc[-1]
        curr_vol = hist['Volume'].iloc[-1]
        avg_vol = hist['Volume'].rolling(window=20).mean().iloc[-1]

        # 條件：站上月線 (MA20) 且 今日成交量大於月均量 1.5 倍 (主力介入跡象)
        if curr_price > ma_20 and curr_vol > avg_vol * 1.5:
            found = True
            msg += f"🚀 **{ticker}**\n"
            msg += f"🔸 現價: ${curr_price:.2f} (站上月線)\n"
            msg += f"🔸 爆量突破: 達均量 {(curr_vol/avg_vol):.1f} 倍\n---\n"

    if not found: msg += "今日重點觀測股無明顯價量突破跡象。\n"
    return msg


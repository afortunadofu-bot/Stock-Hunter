# ================= 台股分析模組 =================
def get_twse_dates(days=5):
    dates = []
    today = datetime.date.today()
    while len(dates) < days:
        if today.weekday() < 5:
            dates.append(today)
        today -= datetime.timedelta(days=1)
    return dates[::-1]

def get_twse_buy(date_str):
    url = f"https://www.twse.com.tw/fund/T86?response=json&date={date_str}&selectType=ALL"
    try:
        res = requests.get(url, headers={'User-agent': 'Mozilla/5.0'})
        data = res.json()
        if data['stat'] == 'OK':
            df = pd.DataFrame(data['data'], columns=data['fields'])
            df['三大法人買賣超股數'] = df['三大法人買賣超股數'].str.replace(',', '').astype(float)
            df['買賣超張數'] = df['三大法人買賣超股數'] / 1000
            return df[['證券代號', '證券名稱', '買賣超張數']]
    except Exception:
        pass
    return pd.DataFrame()

def check_retail_leaving(stock_id):
    """使用 FinMind API 檢查散戶是否退場 (1-50張的持有總人數是否減少)"""
    url = "https://api.finmindtrade.com/api/v4/data"
    parameter = {
        "dataset": "TaiwanStockHoldingSharesPer",
        "data_id": stock_id,
        "start_date": (datetime.date.today() - datetime.timedelta(days=21)).strftime("%Y-%m-%d"),
        "token": FINMIND_TOKEN
    }
    try:
        res = requests.get(url, params=parameter)
        data = res.json()
        if data["status"] == 200 and len(data["data"]) > 0:
            df = pd.DataFrame(data["data"])
            # 取最近兩週的日期
            dates = df['date'].unique()[-2:]
            if len(dates) < 2: return True # 資料不足預設通過

            # 定義散戶：持股分級 1~5 (1股到50張)
            retail_df = df[df['HoldingSharesLevel'].isin([1,2,3,4,5])]
            last_week_retail = retail_df[retail_df['date'] == dates[0]]['people'].sum()
            this_week_retail = retail_df[retail_df['date'] == dates[1]]['people'].sum()

            return this_week_retail < last_week_retail
    except Exception:
        pass
    return True # API失敗時預設放行，以免錯失標的

def analyze_tw_stocks():
    msg = "🇹🇼 **【台股｜主力籌碼嚴選】**\n"
    recent_days = get_twse_dates(CONSECUTIVE_DAYS)
    today_str = recent_days[-1].strftime("%Y%m%d")

    today_data = get_twse_buy(today_str)
    if today_data.empty:
        return msg + "今日台股無資料或尚未更新。\n\n"

    top_5 = today_data.sort_values(by='買賣超張數', ascending=False).head(5)
    candidates = top_5['證券代號'].tolist()
    found = False

    for stock_id in candidates:
        stock_name = top_5[top_5['證券代號'] == stock_id]['證券名稱'].values[0]

        # 1. 散戶過濾
        if not check_retail_leaving(stock_id): continue

        # 2. 連續買超與成本過濾
        buy_vols, prices = [], []
        hist = yf.Ticker(f"{stock_id}.TW").history(period="10d")

        for date in recent_days:
            date_str = date.strftime("%Y%m%d")
            daily = get_twse_buy(date_str)
            time.sleep(2) # 防封鎖

            vol = daily[daily['證券代號'] == stock_id]['買賣超張數'].values[0] if (not daily.empty and stock_id in daily['證券代號'].values) else 0
            buy_vols.append(vol)

            try:
                price = hist.loc[date.strftime("%Y-%m-%d")]['Close']
                prices.append(price)
            except:
                prices.append(0)

        # 檢查遞增
        if any(buy_vols[i] <= buy_vols[i-1] for i in range(1, len(buy_vols))) or any(v <= 0 for v in buy_vols):
            continue

        total_vol = sum(buy_vols)
        avg_cost = sum(v * p for v, p in zip(buy_vols, prices) if p > 0) / total_vol if total_vol > 0 else 0
        curr_price = prices[-1] if prices else 0

        if curr_price > avg_cost * MAX_COST_DEVIATION: continue

        found = True
        msg += f"🎯 **{stock_id} {stock_name}**\n"
        msg += f"🔹 連續 {CONSECUTIVE_DAYS} 日買超遞增\n"
        msg += f"🔹 主力均價: {avg_cost:.2f} | 現價: {curr_price:.2f}\n"
        msg += f"🔹 籌碼狀態: 散戶退場中 ✅\n---\n"

    if not found: msg += "今日無符合【籌碼遞增+未追高+散戶退場】之嚴格標的。\n\n"
    return msg + "\n"

